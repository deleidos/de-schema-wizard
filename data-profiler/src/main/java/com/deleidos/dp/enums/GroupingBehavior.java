package com.deleidos.dp.enums;

/**
 * Enum to define different grouping behavior of objects.  For example, treat each index of an array as a different field,
 * or test the entire array as a single field.
 * @author leegc
 *
 */
public enum GroupingBehavior {
	GROUP_ARRAY_VALUES, UNIQUE_ARRAY_VALUES, NO_STRUCTURE;
}
